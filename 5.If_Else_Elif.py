nota = 85

if nota >= 90:
   print ("Excelente")

elif nota >= 80:
   print ("Muito bom")

elif nota >= 70:
   print ("Bom")

else:
   print ("Precisa melhorar")
   
# Basicamente o que esse código diz é:

# A nota é igual a 85.
# A primeira condição é caso a nota for maior ou igual a 90 - excelente

# Segunda condição: Se a nota for maior ou igual a 80 - Muito bom

# Terceira condição: Se a nota for maior ou igual a 70 - Bom

# Quarta condição: Se nenhuma das alternativas anteriores forem cumpridas - Precisa melhorar